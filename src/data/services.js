import { getTeamMembersByIds } from './team.js';

export function getServiceWithProfessionals(serviceId) {
  const service = services.find(s => s.id === serviceId);
  if (!service) return null;
  
  return {
    ...service,
    professionals: getTeamMembersByIds(service.professionalIds || [])
  };
}

export function getAllServicesWithProfessionals() {
  return services.map(service => ({
    ...service,
    professionals: getTeamMembersByIds(service.professionalIds || [])
  }));
}